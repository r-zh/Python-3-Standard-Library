# Python Rounding, Absolute Value, and Exponents

# round()
myGPA = 3.6
print(round(myGPA)) # round up or down -> lose 4, plus 5
amountOfSalt = 1.4
print(round(amountOfSalt))

apple = -1.2
print(round(apple))
google = -1.6
print(round(google))

# abs()
distanceAway = -13
print(abs(distanceAway))
lengthOfRootInGround = -2.5
print(abs(lengthOfRootInGround))

# pow()
chanceOfTails = 0.5
inARowTails = 3
# First one base, second one  0.5 * 0.5 * 0.5
print(pow(chanceOfTails, inARowTails))

chanceOfOne = .167
inARowOne = 2
print(pow(chanceOfOne, inARowOne))