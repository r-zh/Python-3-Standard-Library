# Math Module Part 2
import math

# Factorial & Square Root
print(math.factorial(3)) # 3 * 2 * 1 = 6
print(math.sqrt(64))

# Greatest Common Denominator GCD
print(math.gcd(52, 8)) # 最大公约数
print(math.gcd(8, 52))

print(8/52)
print(2/13)

# Degrees and Radians
print(math.radians(360))
print(math.pi * 2)
print(math.degrees(math.pi * 2))